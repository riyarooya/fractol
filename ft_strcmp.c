int	ft_strcmp(char *s1, char *s2)
{
	int	bop;

	bop = 0;
	while ((s1[bop] == s2[bop]) && s1[bop] != '\0' && s2[bop] != '\0')
			bop++;
	return (s1[bop] - s2[bop]);
}
